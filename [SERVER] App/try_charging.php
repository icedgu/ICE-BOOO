    # GET parameter from url
    
    $pubKey = $_GET['pubKey'];
    $amount = $_GET['amount'];
    
    
    
    /* EasyBitcoin-PHP
     A simple class for making calls to Bitcoin's API using PHP.
     https://github.com/aceat64/EasyBitcoin-PHP
     
